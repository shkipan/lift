#include "libft.h"

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*s;

	i = 0;
	if ((s = malloc(size + 1)))
	{
		while (i <= size)
		{
			s[i] = '\0';
			i++;
		}
	}
	else
		return (NULL);
	return (s);
}
