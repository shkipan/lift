#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	i;
	char	*str;

	str = (char *)src;
	i = 0;
	while (str[i])
	{
		dst[i] = str[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
